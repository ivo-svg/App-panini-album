export interface Sticker {
  id: string;
  number: number;
  name: string;
  isFoil?: boolean;
  isTeamPhoto?: boolean;
  isShield?: boolean;
}

export interface Team {
  code: string;
  name: string;
  fullName: string;
  primaryColor: string;
  secondaryColor: string;
  accentColor?: string;
  textOnPrimary?: string;
  stickers: Sticker[];
  order: number;
}

function makeStickers(teamCode: string, baseNumber: number, names: string[]): Sticker[] {
  return names.map((name, i) => {
    const number = baseNumber + i;
    const isFoil = name.endsWith('[FOIL]');
    const cleanName = name.replace(' [FOIL]', '');
    const isTeamPhoto = cleanName === 'Foto de Equipo';
    const isShield = cleanName.startsWith('Escudo');
    return {
      id: `${teamCode}${String(number).padStart(2, '0')}`,
      number,
      name: cleanName,
      isFoil,
      isTeamPhoto,
      isShield,
    };
  });
}

