export const ALL_STICKERS: { team: Team; sticker: Sticker }[] = TEAMS.flatMap((team) =>
  team.stickers.map((sticker) => ({ team, sticker }))
);

export const TOTAL_STICKERS = ALL_STICKERS.length;

export function getStickerById(id: string): { team: Team; sticker: Sticker } | undefined {
  return ALL_STICKERS.find((s) => s.sticker.id === id);
}

export function searchStickers(query: string): { team: Team; sticker: Sticker }[] {
  if (!query.trim()) return [];
  const q = query.toLowerCase().trim();
  return ALL_STICKERS.filter(
    ({ team, sticker }) =>
      sticker.id.toLowerCase().includes(q) ||
      sticker.name.toLowerCase().includes(q) ||
      team.fullName.toLowerCase().includes(q) ||
      team.code.toLowerCase().includes(q)
  ).slice(0, 20);
}
